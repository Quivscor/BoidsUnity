public class ObstacleAvoidanceData
{
    public ObstacleAvoidanceData(float weight, float radius, float degreeOfDecay)
    {
        this.weight = weight;
        this.radius = radius;
        this.degreeOfDecay = degreeOfDecay;
    }

    public float weight;
    public float radius;
    public float degreeOfDecay;
}